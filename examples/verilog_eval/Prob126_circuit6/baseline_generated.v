module TopModule (
  input [2:0] a,
  output reg [15:0] q
);

  always @(*) begin
    case (a)
      3'b000: q = 16'b0000000000000000;
      3'b001: q = 16'b0000000000000001;
      3'b010: q = 16'b0000000000000010;
      3'b011: q = 16'b0000000000000100;
      3'b100: q = 16'b0000000000001000;
      3'b101: q = 16'b0000000000010000;
      3'b110: q = 16'b0000000000100000;
      3'b111: q = 16'b0000000001000000;
    endcase
  end

endmodule