import subprocess
import re

def evaluate(code: str) -> dict:
    """
    Evaluates a Verilog module by running it through a simulation and parsing the output.

    Args:
        code: The Verilog code of the module to be evaluated.

    Returns:
        A dictionary with keys: 'accuracy', 'line_count', 'combined_score', 'error'.
    """
    try:
        exec_name = "a.out"  # Name of the executable generated by iverilog
        
        # Create a temporary Verilog file
        with open("candidate.sv", "w") as f:
            f.write(code)

        # Run iverilog
        iverilog_cmd = f"iverilog -g2012 -o {exec_name} candidate.sv testbench.sv ref.sv"
        iverilog_result = subprocess.run(iverilog_cmd, shell=True, capture_output=True, text=True)

        if iverilog_result.returncode != 0:
            return {"accuracy": 0.0, "line_count": 0, "combined_score": 0.0, "error": iverilog_result.stderr}
        
        # Run the simulation
        vvp_cmd = f"./{exec_name}"
        vvp_result = subprocess.run(vvp_cmd, shell=True, capture_output=True, text=True)

        output = vvp_result.stdout

        # Parse the output for mismatches
        match = re.search(r"Mismatches: (\d+) in (\d+)", output)
        if match:
            errors = int(match.group(1))
            clocks = int(match.group(2))
            if errors > 0:
                accuracy = 1.0 - (float(errors) / clocks) if clocks > 0 else 0.0
            else:
                accuracy = 1.0
        else:
            # Check for "FAIL" or "PASS"
            if "FAIL" in output:
                accuracy = 0.0
            elif "PASS" in output:
                accuracy = 1.0
            else:
                accuracy = 1.0

        # Get line count of the candidate code
        line_count = len(code.splitlines())

        # Combined score (simple, can be improved)
        combined_score = accuracy * line_count

        return {"accuracy": accuracy, "line_count": line_count, "combined_score": combined_score, "error": None}

    except Exception as e:
        return {"accuracy": 0.0, "line_count": 0, "combined_score": 0.0, "error": str(e)}
    finally:
        # Clean up temporary files
        import os
        try:
            os.remove("candidate.sv")
            os.remove("a.out")
        except FileNotFoundError:
            pass