import subprocess
import re

def evaluate(code: str) -> dict:
    """
    Evaluates a Verilog module by running it through a simulation and analyzing the output.

    Args:
        code: The Verilog code of the module to be evaluated.

    Returns:
        A dictionary containing the evaluation results:
        - accuracy: A float representing the accuracy of the module (1.0 for pass, 0.0 for fail).
        - line_count: The number of lines in the Verilog code.
        - combined_score: A placeholder for future combined scoring (currently 1.0).
        - error: A string containing any error messages encountered during the process.
    """

    exec_name = "eval_exec"  # Name of the executable generated by iverilog
    testbench_file = "testbench.sv"
    ref_file = "ref.sv"

    try:
        # Write the candidate code to a file
        with open("candidate.sv", "w") as f:
            f.write(code)

        # Compile the Verilog code using iverilog
        compile_command = f"iverilog -g2012 -o {exec_name} candidate.sv {testbench_file} {ref_file}"
        compile_result = subprocess.run(compile_command, shell=True, capture_output=True, text=True)

        if compile_result.returncode != 0:
            return {
                "accuracy": 0.0,
                "line_count": len(code.splitlines()),
                "combined_score": 1.0,
                "error": f"Iverilog compilation error: {compile_result.stderr}"
            }

        # Run the simulation using vvp
        simulation_command = f"./{exec_name}"
        simulation_result = subprocess.run(simulation_command, shell=True, capture_output=True, text=True)

        # Analyze the simulation output
        output = simulation_result.stdout
        line_count = len(code.splitlines())
        accuracy = 1.0
        errors = 0
        mismatches = 0

        if "FAIL" in output:
            accuracy = 0.0

        # Parse mismatch counts from the output
        mismatch_pattern = re.compile(r"Mismatches:\s*(\d+)\s*in\s*(\d+)")
        match = mismatch_pattern.search(output)
        if match:
            errors = int(match.group(1))
            mismatches = int(match.group(2))

        if errors > 0:
            accuracy = 0.0

        if "PASS" in output:
            accuracy = 1.0

        return {
            "accuracy": accuracy,
            "line_count": line_count,
            "combined_score": 1.0,
            "error": ""
        }

    except Exception as e:
        return {
            "accuracy": 0.0,
            "line_count": len(code.splitlines()),
            "combined_score": 1.0,
            "error": str(e)
        }
    finally:
        # Clean up the generated files
        import os
        try:
            os.remove("candidate.sv")
            os.remove(f"{exec_name}")
        except FileNotFoundError:
            pass