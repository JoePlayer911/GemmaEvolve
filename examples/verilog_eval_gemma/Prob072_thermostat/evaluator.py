import subprocess
import re

def evaluate(code: str) -> dict:
    """
    Evaluates a Verilog module by running Iverilog and comparing the output
    with a reference design.

    Args:
        code: The Verilog code of the module to be evaluated.

    Returns:
        A dictionary containing the evaluation results.
    """
    exec_name = "a.out"  # Name of the executable generated by iverilog
    try:
        # Write the candidate module to a file
        with open("candidate.sv", "w") as f:
            f.write(code)

        # Run iverilog
        iverilog_cmd = f"iverilog -g2012 -o {exec_name} candidate.sv testbench.sv ref.sv"
        iverilog_result = subprocess.run(iverilog_cmd, shell=True, capture_output=True, text=True)

        if iverilog_result.returncode != 0:
            return {"accuracy": 0.0, "line_count": 0, "combined_score": 0.0, "error": iverilog_result.stderr}

        # Run the simulation
        vvp_cmd = f"./{exec_name}"
        vvp_result = subprocess.run(vvp_cmd, shell=True, capture_output=True, text=True)

        # Parse the output
        output = vvp_result.stdout
        accuracy = 1.0
        error_msg = ""

        if "FAIL" in output:
            accuracy = 0.0
            mismatches = re.search(r"Mismatches:\s*(\d+)\s*in\s*(\d+)", output)
            if mismatches:
                mismatches_count = int(mismatches.group(1))
                total_count = int(mismatches.group(2))
                accuracy = 1.0 - (float(mismatches_count) / float(total_count))
            else:
                error_msg = "FAIL detected, but no mismatches found in output."

        elif "PASS" in output:
            accuracy = 1.0
        else:
            # Check for errors in the simulation output
            if "errors" in output:
                accuracy = 0.0
                error_msg = "Errors found in simulation output."

        line_count = len(code.splitlines())

        combined_score = accuracy  # Simple combined score for now

        return {"accuracy": accuracy, "line_count": line_count, "combined_score": combined_score, "error": error_msg}

    except Exception as e:
        return {"accuracy": 0.0, "line_count": 0, "combined_score": 0.0, "error": str(e)}
    finally:
        # Clean up generated files
        import os
        try:
            os.remove("candidate.sv")
            os.remove(exec_name)
        except FileNotFoundError:
            pass