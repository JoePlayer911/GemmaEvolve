import subprocess
import re

def evaluate(code: str) -> dict:
    """
    Evaluates a Verilog module by simulating it against a reference model.

    Args:
        code: The Verilog code of the module to be evaluated.

    Returns:
        A dictionary with keys: 'accuracy', 'line_count', 'combined_score', 'error'.
    """
    try:
        exec_name = "a.out"  # Name of the executable generated by iverilog
        candidate_file = "candidate.sv"
        ref_file = "testbench.sv" # Assumes testbench.sv contains ref.sv

        with open(candidate_file, "w") as f:
            f.write(code)

        # Compile the Verilog code using iverilog
        compile_process = subprocess.run(
            ["iverilog", "-g2012", "-o", exec_name, candidate_file, ref_file],
            capture_output=True,
            text=True
        )
        if compile_process.returncode != 0:
            return {
                "accuracy": 0.0,
                "line_count": len(code.splitlines()),
                "combined_score": 0.0,
                "error": f"Compilation error: {compile_process.stderr}"
            }

        # Run the simulation using vvp
        sim_process = subprocess.run(
            [exec_name],
            capture_output=True,
            text=True
        )

        output = sim_process.stdout
        
        # Check for "FAIL"
        if "FAIL" in output:
            mismatches = re.search(r"Mismatches: (\d+) in (\d+)", output)
            if mismatches:
                errors = int(mismatches.group(1))
                clocks = int(mismatches.group(2))
                accuracy = 1.0 - (errors / clocks) if clocks > 0 else 0.0
            else:
                accuracy = 0.0
        
        # Check for "PASS" or no errors
        elif "PASS" in output:
            accuracy = 1.0
        else:
            mismatches = re.search(r"Mismatches: (\d+) in (\d+)", output)
            if mismatches:
                errors = int(mismatches.group(1))
                clocks = int(mismatches.group(2))
                accuracy = 1.0 - (errors / clocks) if clocks > 0 else 0.0
            else:
                accuracy = 1.0

        return {
            "accuracy": accuracy,
            "line_count": len(code.splitlines()),
            "combined_score": accuracy,
            "error": None
        }

    except Exception as e:
        return {
            "accuracy": 0.0,
            "line_count": len(code.splitlines()),
            "combined_score": 0.0,
            "error": str(e)
        }
    finally:
        # Clean up the generated files
        import os
        try:
            os.remove(candidate_file)
            os.remove(exec_name)
        except FileNotFoundError:
            pass